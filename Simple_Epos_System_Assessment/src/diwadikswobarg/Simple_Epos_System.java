    private void btnDownloadReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDownloadReportActionPerformed
        LocalDateTime localdatetime = LocalDateTime.ofInstant(Instant.now(),ZoneId.systemDefault());
        String filenameDate = localdatetime.getDayOfWeek() + " " + localdatetime.getDayOfMonth() + " " + localdatetime.getMonth() + " " + localdatetime.getYear();
        String filename =  filenameDate + " Customer Report.txt";
        JFileChooser fileChooser = new JFileChooser(System.getProperty("user.home") + "\\Desktop");
        fileChooser.setDialogTitle("Save Report");
        fileChooser.setSelectedFile(new File(filename));
        int userSelection = fileChooser.showSaveDialog(this);
        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();
                try {
                    FileWriter writer = new FileWriter(fileToSave);
                    writer.write(txtAreaReport.getText());
                    writer.close();
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(this, "Error saving report", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_btnDownloadReportActionPerformed

    
    private void updateTotal() {
        Product selectedItem = (Product)cmbProductPrice.getSelectedItem();
        int quantity = (int)cmbQuantity.getSelectedItem();
        if(selectedItem != null)
        {
            lblSelectedTotal.setText(quantity + " x £" + selectedItem.Price + " = " + String.format("%.2f", quantity * selectedItem.Price));
        }
    }

