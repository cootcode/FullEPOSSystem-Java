    private double highestSale;
    private double lowestSale;
    private double totalValueOfSales = 0;
    private int i = 1;
    private void btnCheckOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckOutActionPerformed
        //message box display to user with detailed informations about bought items
        if(txtAreaShoppingCart.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Add an item to the cart to continue!");
            return;
        }else
        {
        JOptionPane.showMessageDialog(
                null, 
                "Products List:\n"  + txtAreaShoppingCart.getText()
                +"\n Cost Total: \n"+ lblSalesTotal.getText(),
                "Check Out", 1);
        }
        //take all the text area box informations and put it into the raport as customer<number>

            if(txtAreaReport.getText().isEmpty())
            {
            txtAreaReport.append( 
                    "Customer " + i
                    + "\n" 
                    + txtAreaShoppingCart.getText() 
                    + "\n" 
                    + lblSalesTotal.getText()
                    +"\n"
                    +"-------------------------------");
                    i++;
            }else
            {
                    txtAreaReport.append( 
                    "\nCustomer " + i
                    + "\n" 
                    + txtAreaShoppingCart.getText() 
                    + "\n" 
                    + lblSalesTotal.getText()
                    +"\n"
                    +"-------------------------------");
                    i++;
            }

            totalValueOfSales += basket.GetTotalPrice();
            //change text of report labels
            lblTotalNumberOfSales.setText("Total No of Sales: " + (i - 1));
            lblTotalSalesReport.setText("Total Sales: " + totalValueOfSales);
            lblTotalCustomers.setText("Total Customers: " + (i-1));
            lblAverageSale.setText("Average Sale: " + (totalValueOfSales / (i-1)));
            if (basket.GetTotalPrice() > highestSale)
            {
                highestSale = basket.GetTotalPrice();
            }
            if (basket.GetTotalPrice() < lowestSale || lowestSale == 0)
            {
                lowestSale = basket.GetTotalPrice();
            }
            lblMinimumSale.setText("Lowest Sale: " + lowestSale);
            lblHighestSale.setText("Highest Sale: " + highestSale);
            
            //clear the basket and shopping cart after adding to report/checking out
            txtAreaShoppingCart.setText("");
            lblSalesTotal.setText("£0.00");
            basket.ClearBasket();
    }//GEN-LAST:event_btnCheckOutActionPerformed

